#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int size;
	int i;

	size = ft_strlen(src);
	i = 0;
	while (i < size)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
